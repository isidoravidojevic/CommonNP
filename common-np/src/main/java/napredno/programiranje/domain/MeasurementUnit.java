package napredno.programiranje.domain;

/**
 * Enumeracija koja predstavlja razlicite jedinice mere, kao sto su kilogram (KG), litra (L),
 * gram (G) i komad (PSC).
 * 
 * @author Isidora Vidojevic
 * 
 */

public enum MeasurementUnit {
	
	/**
     * Kilogram (KG) kao jedinica mere.
     */
	KG,
	
	/**
     * Litar (L) kao jedinica mere.
     */
	L,
	
	/**
     * Gram (G) kao jedinica mere.
     */
	G,
	
	/**
     * Komad (PSC) kao jedinica mere.
     */
	PSC;
}
